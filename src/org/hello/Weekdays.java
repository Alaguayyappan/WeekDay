package org.hello;

public class Weekdays {

	public void monDay() {
		System.out.println("monday");
	}

	public void tuesDay() {
		System.out.println("tuesday");
	}

	public void wednesDay() {
		System.out.println("wednesday");

	}

	public static void main(String[] args) {
		Weekdays week = new Weekdays();
		week.monDay();
		week.tuesDay();
		week.wednesDay();

	}

}